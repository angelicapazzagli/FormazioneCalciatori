    private void setAllenatore() {
        int i = cmbAllenatore.getSelectedIndex();
        if(i == 0) {
            return;
        }
        String all = cmbAllenatore.getSelectedItem().toString();
        Allenatore a;
        String path;

        switch(all) {
            case "PREPARATORE ATLETICO [pac]":
                a = new Allenatore(Abilità.PAC, "ZIDANE");
                path = "/immagini/Zidane.png";
                break;
            case "PREPARATORE PIAZZATI [sho]":
                a = new Allenatore(Abilità.SHO, "ALLEGRI");
                path = "/immagini/Allegri.png";
                break;
            case "PREPARATORE TECNICO [pas]":
                a = new Allenatore(Abilità.PAS, "ANCELOTTI");
                path = "/immagini/Ancelotti.png";
                break;
            case "PREPARATORE INDIVIDUALE [dri]":
                a = new Allenatore(Abilità.DRI, "GUARDIOLA");
                path = "/immagini/Guardiola.png";
                break;
            case "PREPARATORE TATTICO [def]":
                a = new Allenatore(Abilità.DEF, "SPALLETTI");
                path = "/immagini/Spalletti.png";
                break;
            case "PREPARATORE FISICO [phy]":
                a = new Allenatore(Abilità.PHY, "CONTE");
                path = "/immagini/Conte.png";
                break;
            default:
                return;
        }
        ImageIcon icon = new ImageIcon(getClass().getResource(path));
        Image img = icon.getImage().getScaledInstance(64, 64, Image.SCALE_SMOOTH);
        a.setIcon(new ImageIcon(img));
        if(squadra.addAllenatore(a)) {
            aggiornaLabelAllenatori();
            cmbAllenatore.setSelectedIndex(0);
        }
    }
    
    private void setAllenamento() {
        int i = cmbAllenamento.getSelectedIndex();
        if(i == 0) {
            return;
        }
        String all = cmbAllenamento.getSelectedItem().toString();
        Allenamento al = null;

        switch(all) {
            case "SPRINT [pac]":
                al = new Sprint(10);
                break;
            case "CALCI PIAZZATI [sho]":
                al = new CalcioPiazzato(20, true);
                break;
            case "TRIANGOLAZIONI [pas]":
                al = new Triangolazione();
                break;
            case "SLALOM [dri]":
                al = new Slalom();
                break;
            case "UNO CONTRO UNO [def]":
                al = new UnoControUno(77);
                break;
            case "CIRCUITI [phy]":
                al = new Circuito(30, 3);
                break;
            default:
                return;
        }
        if(squadra.addAllenamento(al)) {
            aggiornaLabelAllenamenti();
            cmbAllenamento.setSelectedIndex(0);
        }
    }
    
    private void aggiornaLabelAllenatori() {
        if (squadra.getAllenatori().size() > 0) {
            lblAllenatore1.setIcon(squadra.getAllenatori().get(0).getIcon());
        }
        else {
            lblAllenatore1.setIcon(null);
        }
        if (squadra.getAllenatori().size() > 1) {
            lblAllenatore2.setIcon(squadra.getAllenatori().get(1).getIcon());
        }
        else {
            lblAllenatore2.setIcon(null);
        }
    }
    
    private void aggiornaLabelAllenamenti() {
        if (squadra.getAllenamenti().size() > 0){
            lblAllenamento1.setText(squadra.getAllenamenti().get(0).toString());
        }
        else {
            lblAllenamento1.setText("");
        }
        if (squadra.getAllenamenti().size() > 1) {
            lblAllenamento2.setText(squadra.getAllenamenti().get(1).toString());
        }
        else {
            lblAllenamento2.setText("");
        }
    }
    
    private void aggiornaStatistiche() {
        lblAbilità.setText(calciatore.getAbilità());
        lblOverall.setText(calciatore.getOverall());
    }
